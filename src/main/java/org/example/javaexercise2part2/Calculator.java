package org.example.javaexercise2part2;

public class Calculator {

    public Calculator(){

    }

    public int add(int x, int y){
        return x + y;
    }

    public int subtract(int x, int y){
        return  x - y;
    }

    public double multiply(double x, double y){
        return x * y;
    }

    public double divide(double x, double y){
        return x / y;
    }
}
